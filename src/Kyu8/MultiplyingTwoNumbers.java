package Kyu8;

public class MultiplyingTwoNumbers {
    public static void main(String[] args) {
        int num1, num2;
        num1 = 2;
        num2 = 2;
        System.out.println(multiply(num1, num2));
    }

    public static int multiply(int num1, int num2) {
        return num1 * num2;
    }
}
